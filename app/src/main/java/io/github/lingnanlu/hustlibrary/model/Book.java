public class Book {

    private String mTitle;
    private String mAuthor;
    private String mTranslator;
    private String mPress;
    private String mAbstracts;
    private String mISBN;
    private String mLocation;
    private String mCallNumber;

    public String getmCallNumber() {
        return mCallNumber;
    }

    public void setmCallNumber(String mCallNumber) {
        this.mCallNumber = mCallNumber;
    }

    public String getmISBN() {
        return mISBN;
    }

    public void setmISBN(String mISBN) {
        this.mISBN = mISBN;
    }

    public String getmLocation() {
        return mLocation;
    }

    public void setmLocation(String mLocation) {
        this.mLocation = mLocation;
    }

    public String getmAbstracts() {
        return mAbstracts;
    }

    public void setmAbstracts(String mAbstracts) {
        this.mAbstracts = mAbstracts;
    }

    public String getmPress() {
        return mPress;
    }

    public void setmPress(String mPress) {
        this.mPress = mPress;
    }

    public String getmTranslator() {
        return mTranslator;
    }

    public void setmTranslator(String mTranslator) {
        this.mTranslator = mTranslator;
    }

    public String getmAuthor() {
        return mAuthor;
    }

    public void setmAuthor(String mAuthor) {
        this.mAuthor = mAuthor;
    }

    public String getmTitle() {
        return mTitle;
    }

    public void setmTitle(String mTitle) {
        this.mTitle = mTitle;
    }





}
